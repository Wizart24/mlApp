
using Microsoft.EntityFrameworkCore;
using Microsoft.Extensions.Configuration;
using MLAppAPI.Entity;
using MLAppAPI.Services;
using MLAppAPI.Services.Interfaces;

namespace MLAppAPI
{
    public class Program
    {
        public static void Main(string[] args)
        {
            var builder = WebApplication.CreateBuilder(args);

            // Add services to the container.
            builder.Services.AddDbContext<DataFileDbContext>(options =>
                options.UseSqlServer(builder.Configuration.GetConnectionString("Prod")));

            builder.Services.AddControllers();
            // Learn more about configuring Swagger/OpenAPI at https://aka.ms/aspnetcore/swashbuckle
            builder.Services.AddEndpointsApiExplorer();
            builder.Services.AddSwaggerGen();
            builder.Services.AddSingleton<IModelService, ModelService>();
            builder.Services.AddScoped<IFileUploaderService, FileUploaderService>();

            var app = builder.Build();

            // Configure the HTTP request pipeline.
            if (app.Environment.IsDevelopment())
            {
                app.UseSwagger();
                app.UseSwaggerUI();
            }

            app.UseHttpsRedirection();

            app.UseAuthorization();


            app.MapControllers();

            app.Run();
        }
    }
}
